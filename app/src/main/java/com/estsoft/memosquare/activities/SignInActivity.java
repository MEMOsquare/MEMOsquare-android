    @Override
    public void onConnectionFailed(ConnectionResult connectionResult) {
        // An unresolvable error has occurred and Google APIs (including Sign-In) will not
        // be available.
        Log.d(TAG, "onConnectionFailed:" + connectionResult);
    }

    @Override
    public void onClick(View v) {
        switch (v.getId()) {
            case R.id.sign_in_button:
                signIn();
                break;
        }
    }

    private void signIn() {
        Intent signInIntent = Auth.GoogleSignInApi.getSignInIntent(mGoogleApiClient);
        startActivityForResult(signInIntent, RC_SIGN_IN);
        mGoogleApiClient.connect();
    }

    @Override
    public void onActivityResult(int requestCode, int resultCode, Intent data) {
        super.onActivityResult(requestCode, resultCode, data);

        // Result returned from launching the Intent from GoogleSignInApi.getSignInIntent(...);
        if (requestCode == RC_SIGN_IN) {
            GoogleSignInResult result = Auth.GoogleSignInApi.getSignInResultFromIntent(data);
            handleSignInResult(result);
        }
    }

    private void handleSignInResult(GoogleSignInResult result) {
        Log.d(TAG, "handleSignInResult:" + result.isSuccess());

        if (result.isSuccess()) {
            // Signed in successfully, show authenticated UI.
            GoogleSignInAccount acct = result.getSignInAccount();

            Toast.makeText(this,"Signed in: "+acct.getDisplayName(), Toast.LENGTH_SHORT).show();
            Log.d(TAG, acct.getDisplayName());
            Log.d(TAG, "여기까지 되었다");

            //Send the ID token to your server
            String idToken = acct.getIdToken();
            Log.d(TAG, "얍얍"+idToken);

            Toast.makeText(this,"ID Token: " + idToken, Toast.LENGTH_SHORT).show();

            new myAsyc().execute("http://192.168.22.71/signin/", idToken);

        } else {
            // Signed out, show unauthenticated UI.
            Toast.makeText(this,"ID Token: null", Toast.LENGTH_SHORT).show();


        }
    }

    private void showProgressDialog() {
        if (mProgressDialog == null) {
            mProgressDialog = new ProgressDialog(this);
            mProgressDialog.setMessage("Loading...");
            mProgressDialog.setIndeterminate(true);
        }

        mProgressDialog.show();
    }

    private void hideProgressDialog() {
        if (mProgressDialog != null && mProgressDialog.isShowing()) {
            mProgressDialog.hide();
        }
    }

    public class myAsyc extends AsyncTask<String, Integer, Long>{

        @Override
        protected Long doInBackground(String... strings) {
            // TODO(user): send token to server and validate server-side
            HttpClient httpClient = new DefaultHttpClient();
            String url = strings[0];
            HttpPost httpPost = new HttpPost(url);

            try {
                String authCode = strings[1];
                List nameValuePairs = new ArrayList(1);
                nameValuePairs.add(new BasicNameValuePair("authCode", authCode));
                httpPost.setEntity(new UrlEncodedFormEntity(nameValuePairs));

                HttpResponse response = httpClient.execute(httpPost);
                int statusCode = response.getStatusLine().getStatusCode();
                final String responseBody = EntityUtils.toString(response.getEntity());
                Log.i(TAG, "Signed in as: " + responseBody);
            } catch (ClientProtocolException e) {
                Log.e(TAG, "Error sending ID token to backend.", e);
            } catch (IOException e) {
                Log.e(TAG, "Error sending ID token to backend.", e);
            }
            return 0L;
        }
    }

